    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nameOrCusID = txt_searchFeildInView.getText();
        Model.Customer cus  = new Model.Customer();
        cus.searchUsers(nameOrCusID);
        Boolean qs = cus.returnQueryStatus();
        if(qs == true){
            lbl_customerName.setText(cus.Name);
            lbl_customerID.setText(cus.CID);
            lbl_NIC.setText(cus.NIC);
            lbl_email.setText(cus.email);
            lbl_telephone.setText(cus.tel);
            lbl_address.setText(cus.address);

        }else{
            lbl_searchNotFoundinView.setText("Invalid Customer Name/ID or Customer Not Exist");
            lbl_searchNotFoundinView.setForeground(Color.red);
            lbl_searchNotFoundinView.setVisible(true);
            lbl_customerName.setText("N/A");
            lbl_customerID.setText("N/A");
            lbl_NIC.setText("N/A");
            lbl_email.setText("N/A");
            lbl_telephone.setText("N/A");
            lbl_address.setText("N/A");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

