    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        lbl_NICValidation.setVisible(false);
        lbl_addressValidation.setVisible(false);
        lbl_nameValidation.setVisible(false);
        lbl_emailValidation.setVisible(false);
        lbl_telephoneValidation.setVisible(false);
        lbl_searchNotFoundinView.setVisible(false);

        try{
            dbConnection con = new dbConnection();
            con.createConnection();
            String query = "select * from customer order by cID DESC";
            PreparedStatement pst = con.con.prepareStatement(query);
            ResultSet rs = pst.executeQuery();
            while (rs.next()){
                String CID = rs.getString("cID");
                String Name = rs.getString("title")+ " " + rs.getString("first_name") + " " + rs.getString("last_name");
                String email = rs.getString("email");
                String tel = rs.getString("tel");
                String NIC = rs.getString("nic");

                String tbData[] = {CID,Name,tel,email,NIC};

                DefaultTableModel tblModel = (DefaultTableModel)tbl_customerData.getModel();
                tblModel.addRow(tbData);
            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(null,e);
        }

    }//GEN-LAST:event_formWindowOpened

    private void btn_addCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addCustomerActionPerformed
        // TODO add your handling code here:
        if (txt_firstName.getText().trim().isEmpty() && txt_lastName.getText().trim().isEmpty()){
            lbl_nameValidation.setText("Name Can't be Empty!");
            lbl_nameValidation.setVisible(true);
        }else{
            countCorrect = countCorrect + 1;
        }

        if(txt_tel.getText().trim().isEmpty()){
            lbl_telephoneValidation.setText("Telephone Number can't be Empty!");
            lbl_telephoneValidation.setVisible(true);
        }else{
            String pattern = "(?:7|0|(?:\\+94))[0-9]{9,10}$";
            Pattern r = Pattern.compile(pattern);
            Matcher m = r.matcher(txt_tel.getText());
            if (m.find()){
                countCorrect = countCorrect + 1;   
            }else{
                lbl_telephoneValidation.setText("Telephone Number is not valid!");
                lbl_telephoneValidation.setVisible(true);
            }
        }

        if(txt_email.getText().trim().isEmpty()){
            lbl_emailValidation.setText("E-Mail Address can't be Empty!");
            lbl_emailValidation.setVisible(true);
        }else{
            String pattern = "^[A-Za-z0-9+_.-]+@(.+)$";
            Pattern email = Pattern.compile(pattern);
            Matcher m = email.matcher(txt_email.getText());
            if (m.find()){
                countCorrect = countCorrect + 1;
            }else{
                lbl_emailValidation.setText("Email address not valid!");
                lbl_emailValidation.setVisible(true);
            }
        }

        if(txt_address.getText().trim().isEmpty()){
            lbl_addressValidation.setText("Address can't be Empty!");
            lbl_addressValidation.setVisible(true);
        }else{
            countCorrect = countCorrect + 1;
        }

        if(txt_nic.getText().trim().isEmpty()){
            lbl_NICValidation.setText("NIC Number can't be Empty!");
            lbl_NICValidation.setVisible(true);
        }else{
            String pattern = "^([0-9]{9}[x|X|v|V]|[0-9]{12})$";
            Pattern nic = Pattern.compile(pattern);
            Matcher m = nic.matcher(txt_nic.getText());
            if(m.find()){
                countCorrect = countCorrect + 1;            
            }else{
                lbl_NICValidation.setText("NIC Number is not Valid!");
                lbl_NICValidation.setVisible(true);
            }
        }

        if (countCorrect == 5){
            String title;
            if(txt_title.getSelectedIndex() == 0){
                title = "Mr.";
            }else if (txt_title.getSelectedIndex() == 1){
                title = "Ms."; 
            }else{
                title = "Mrs.";
            }
            String fName = txt_firstName.getText();
            String lName = txt_lastName.getText();
            String email = txt_email.getText();
            String nic = txt_nic.getText();
            String tel = txt_tel.getText();
            String address = txt_address.getText();

            Model.Customer cus = new Model.Customer();
            cus.insertCustomerData(title,fName,lName,email,tel,nic,address);
            Boolean qs = cus.returnQueryStatus();

            if(qs == true){
                VDone done = new VDone();
                done.setVisible(true);
            }else{
                JOptionPane.showMessageDialog(null,"Operaion Faild, Error with Databse Connection");
            }
        }else{
            countCorrect = 0;
        }
    }//GEN-LAST:event_btn_addCustomerActionPerformed

