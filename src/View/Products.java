    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        lbl_ProductNameValidation.setVisible(false);
        lbl_descriptionValidation.setVisible(false);
        lbl_inventryValidation.setVisible(false);
        lbl_priceValidation.setVisible(false);
        lbl_searchNotFoundinView.setVisible(false);

        try{
            dbConnection con = new dbConnection();
            con.createConnection();
            String query = "select * from product";
            PreparedStatement pst = con.con.prepareStatement(query);
            ResultSet rs = pst.executeQuery();
            while (rs.next()){
                String PID = rs.getString("pID");
                String Name = rs.getString("ProductName");
                String price = rs.getString("price");
                String description = rs.getString("Description");

                String tbData[] = {PID,Name,price,description};

                DefaultTableModel tblModel = (DefaultTableModel)tbl_product.getModel();
                tblModel.addRow(tbData);
            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_formWindowOpened

