    private void btn_addProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addProductActionPerformed
        // TODO add your handling code here:
        int count = 0;

        if(txt_description.getText().trim().isEmpty()){
            lbl_descriptionValidation.setText("Description can't be Empty!");
            lbl_descriptionValidation.setVisible(true);
        }else{
            count++;
        }

        if(txt_inventry.getText().trim().isEmpty()){
            lbl_inventryValidation.setText("Inventry cant be Empty!");
            lbl_inventryValidation.setVisible(true);
        }else{
            count++;
        }

        if(txt_price.getText().trim().isEmpty()){
            lbl_priceValidation.setText("Price can't be Empty!");
            lbl_priceValidation.setVisible(true);
        }else{
            int price = Integer.parseInt(txt_price.getText());
            if (price < 0){
                lbl_priceValidation.setText("Price can't be less than Zero!");
                lbl_priceValidation.setVisible(true);
            }else{
                count++;
            }
        }

        if(txt_prodName.getText().trim().isEmpty()){
            lbl_ProductNameValidation.setText("Product Name can't be Empty!");
            lbl_ProductNameValidation.setVisible(true);
        }else{
            count++;
        }

        if(count == 4){
            Model.Product prod = new Model.Product();
            prod.insertProduct(txt_prodName.getText(), txt_price.getText(), txt_description.getText(), txt_inventry.getText());
            Boolean st = prod.returnQueryStatus();
            if (st == true){
                VDone done = new VDone();
                done.setVisible(true);
            }else{
                JOptionPane.showMessageDialog(null,"Operaion Faild, Error with Databse Connection");
            }            
        }else{
            
        } 
    }//GEN-LAST:event_btn_addProductActionPerformed

    private void btn_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_searchActionPerformed
        // TODO add your handling code here:
        String nameOrID = txt_IDorNAME.getText();
        Model.Product prod  = new Model.Product();
        prod.searchProduct(nameOrID);
        Boolean qs = prod.returnQueryStatus();
        if(qs == true){
            lbl_productName.setText(prod.Name);
            lbl_productID.setText(prod.PID);
            lbl_price.setText(prod.price);
            lbl_description.setText(prod.description);
            prod.searchStocks();
            lbl_status.setText(prod.stockStatus);
            lbl_stocks.setText(prod.stocoCount);
            lbl_searchNotFoundinView.setVisible(false);
        }else{
            lbl_searchNotFoundinView.setVisible(true);
            lbl_productName.setText("N/A");
            lbl_productID.setText("N/A");
            lbl_price.setText("N/A");
            lbl_stocks.setText("N/A");
            lbl_status.setText("N/A");
            lbl_description.setText("N/A");
        }
    }//GEN-LAST:event_btn_searchActionPerformed

